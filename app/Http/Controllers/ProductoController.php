    {
        $productos = DB::table('productos as p')
                    ->join('categorias as c', 'c.id','p.id_categoria')
                    ->where('p.estado','a')
                    ->select('p.id as idp','p.nombre as producto','p.descripcion','c.nombre as categoria','p.precio as precio','p.estado as estado')
                    ->orderBy('p.id','asc')
                    ->get();

